#String Methods:Casing
str1="banana"
print(str1.upper())#for uppercase
print(str1.lower())#will make everyletter lowercase
print(str1.capitalize())#will make first letter uppercase while rest  lowercase
print(str1.isupper())#will give true if everyletter is uppercase otherwise false
print(str1.islower())#will give false if everyletter is lowercase

